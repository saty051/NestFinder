// Defining the User interface to represent user-related information.
export interface User {
    // Username of the user; used for user identification and login.
    userName: string;

    // Email address of the user; used for communication and login.
    email: string;

    // Password for the user's account; used for authentication.
    password: string;

    // Mobile number of the user; used for contact and verification purposes.
    mobile: number;
}
